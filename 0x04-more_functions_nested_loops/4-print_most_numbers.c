#include "main.h"
 /**
  * Description : this function prints numbers from 0-9 except 2 and 4
  * 4-print_most_numbers - prints numbers
  * Return : 0 indicats progrsam is executed successfully
  */

void print_most_numbers(void);

void print_most_numbers(void)
{
	int i;

	for (i = 0; i <= 0; i++)
	{
		if ((i != 2) && (i != 4))
		{
			_putchar(i + '0');
		}
		_putchar('\n');
	}
}
